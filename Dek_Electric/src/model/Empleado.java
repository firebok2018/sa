package model;

public class Empleado {
	// Atributos
	private int cod_emp, cod_dis;
	private String tip_emp, nom_emp, ape_emp, dni_emp,fna_emp, tel_emp, dir_emp, fin_emp;
	
	// Operaciones get y set
	public int getCod_emp() {
		return cod_emp;
	}
	public int getCod_dis() {
		return cod_dis;
	}
	public String getTip_emp() {
		return tip_emp;
	}
	public String getNom_emp() {
		return nom_emp;
	}
	public String getApe_emp() {
		return ape_emp;
	}
	public String getDni_emp() {
		return dni_emp;
	}
	public String getFna_emp() {
		return fna_emp;
	}
	public String getTel_emp() {
		return tel_emp;
	}
	public String getDir_emp() {
		return dir_emp;
	}
	public String getFin_emp() {
		return fin_emp;
	}
	public void setCod_emp(int cod_emp) {
		this.cod_emp = cod_emp;
	}
	public void setCod_dis(int cod_dis) {
		this.cod_dis = cod_dis;
	}
	public void setTip_emp(String tip_emp) {
		this.tip_emp = tip_emp;
	}
	public void setNom_emp(String nom_emp) {
		this.nom_emp = nom_emp;
	}
	public void setApe_emp(String ape_emp) {
		this.ape_emp = ape_emp;
	}
	public void setDni_emp(String dni_emp) {
		this.dni_emp = dni_emp;
	}
	public void setFna_emp(String fna_emp) {
		this.fna_emp = fna_emp;
	}
	public void setTel_emp(String tel_emp) {
		this.tel_emp = tel_emp;
	}
	public void setDir_emp(String dir_emp) {
		this.dir_emp = dir_emp;
	}
	public void setFin_emp(String fin_emp) {
		this.fin_emp = fin_emp;
	}
}
