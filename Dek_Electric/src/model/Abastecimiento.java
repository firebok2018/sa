package model;

public class Abastecimiento {
	// Atributos
	private int cod_prv, cod_pro;
	private double pre_aba;
	// Operaciones get y set
	
	public int getCod_prv() {
		return cod_prv;
	}
	public int getCod_pro() {
		return cod_pro;
	}
	public double getPre_aba() {
		return pre_aba;
	}
	public void setCod_prv(int cod_prv) {
		this.cod_prv = cod_prv;
	}
	public void setCod_pro(int cod_pro) {
		this.cod_pro = cod_pro;
	}
	public void setPre_aba(double pre_aba) {
		this.pre_aba = pre_aba;
	}
	
}
