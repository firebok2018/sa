package model;

public class Consulta_Abastecimiento {
	private String ruc_prv, rso_prv, des_pro;
	private double pre_aba;
	public String getRuc_prv() {
		return ruc_prv;
	}
	public void setRuc_prv(String ruc_prv) {
		this.ruc_prv = ruc_prv;
	}
	public String getRso_prv() {
		return rso_prv;
	}
	public void setRso_prv(String rso_prv) {
		this.rso_prv = rso_prv;
	}
	public String getDes_pro() {
		return des_pro;
	}
	public void setDes_pro(String des_pro) {
		this.des_pro = des_pro;
	}
	public double getPre_aba() {
		return pre_aba;
	}
	public void setPre_aba(double pre_aba) {
		this.pre_aba = pre_aba;
	}
}
