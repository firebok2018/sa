package model;

public class DetalleVenta {
	// Atributos
	private int num_ven, cod_pro, can_ven;
	
	// Operaciones get y set
	public int getNum_ven() {
		return num_ven;
	}

	public int getCod_pro() {
		return cod_pro;
	}

	public int getCan_ven() {
		return can_ven;
	}

	public void setNum_ven(int num_ven) {
		this.num_ven = num_ven;
	}

	public void setCod_pro(int cod_pro) {
		this.cod_pro = cod_pro;
	}

	public void setCan_ven(int can_ven) {
		this.can_ven = can_ven;
	}	
}
