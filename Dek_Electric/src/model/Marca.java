package model;

public class Marca {
	// Atributos
	private int cod_mar;
	private String des_mar;
	
	// Operaciones get y set
	public int getCod_mar() {
		return cod_mar;
	}
	public String getDes_mar() {
		return des_mar;
	}
	public void setCod_mar(int cod_mar) {
		this.cod_mar = cod_mar;
	}
	public void setDes_mar(String des_mar) {
		this.des_mar = des_mar;
	}	
}
