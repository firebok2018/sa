package model;

public class Proveedor {
	// Atributos
	private int cod_prv;
	private String rso_prv, ruc_prv, dir_prv, tel_prv, ema_prv, rep_prv;
	
	// Operaciones get y set
	public int getCod_prv() {
		return cod_prv;
	}
	public String getRso_prv() {
		return rso_prv;
	}
	public String getRuc_prv() {
		return ruc_prv;
	}
	public String getDir_prv() {
		return dir_prv;
	}
	public String getTel_prv() {
		return tel_prv;
	}
	public String getEma_prv() {
		return ema_prv;
	}
	public String getRep_prv() {
		return rep_prv;
	}
	public void setCod_prv(int cod_prv) {
		this.cod_prv = cod_prv;
	}
	public void setRso_prv(String rso_prv) {
		this.rso_prv = rso_prv;
	}
	public void setRuc_prv(String ruc_prv) {
		this.ruc_prv = ruc_prv;
	}
	public void setDir_prv(String dir_prv) {
		this.dir_prv = dir_prv;
	}
	public void setTel_prv(String tel_prv) {
		this.tel_prv = tel_prv;
	}
	public void setEma_prv(String ema_prv) {
		this.ema_prv = ema_prv;
	}
	public void setRep_prv(String rep_prv) {
		this.rep_prv = rep_prv;
	}	
}
