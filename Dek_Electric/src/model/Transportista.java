package model;

public class Transportista {
	// Atributos
	private int cod_tra;
	private String rso_tra, ruc_tra, con_tra, tel_tra;
	// Operaciones get y set
	
	public int getCod_tra() {
		return cod_tra;
	}
	public String getRso_tra() {
		return rso_tra;
	}
	public String getRuc_tra() {
		return ruc_tra;
	}
	public String getCon_tra() {
		return con_tra;
	}
	public String getTel_tra() {
		return tel_tra;
	}
	public void setCod_tra(int cod_tra) {
		this.cod_tra = cod_tra;
	}
	public void setRso_tra(String rso_tra) {
		this.rso_tra = rso_tra;
	}
	public void setRuc_tra(String ruc_tra) {
		this.ruc_tra = ruc_tra;
	}
	public void setCon_tra(String con_tra) {
		this.con_tra = con_tra;
	}
	public void setTel_tra(String tel_tra) {
		this.tel_tra = tel_tra;
	}	
}
