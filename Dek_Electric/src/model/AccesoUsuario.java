package model;

public class AccesoUsuario {
	// Atributos
	private String ape_emp, nom_emp, log_usu;
	
	// Operaciones get y set
	public String getApe_emp() {
		return ape_emp;
	}

	public String getNom_emp() {
		return nom_emp;
	}

	public String getLog_usu() {
		return log_usu;
	}

	public void setApe_emp(String ape_emp) {
		this.ape_emp = ape_emp;
	}

	public void setNom_emp(String nom_emp) {
		this.nom_emp = nom_emp;
	}

	public void setLog_usu(String log_usu) {
		this.log_usu = log_usu;
	}
	
}
